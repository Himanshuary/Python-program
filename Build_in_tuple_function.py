
tuple1 = (10, 20, 30, 40, 50)
# Python Tuple len() Method
print("Find the length of tuple: ", len(tuple1))

# Python Tuple max() Method
print("Find out the maximum value in tuple: ", max(tuple1))

# Python Tuple min() Method
print("Find out the minimum value in tuple: ", min(tuple1))

# Python Tuple tuple(seq) Method convert a list into a tuple
list1 = [1, 2, 3, 4, 5]
tuple2 = tuple(list1)
print("Tuple is: ", tuple2)